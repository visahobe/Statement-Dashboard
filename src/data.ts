function parseStringArrayToTransactions(arr: string[]): Transaction[] {
  return arr.map((row, idx) => {
    const parts = row.split("|");
    const date = parts[0];
    const desc = parts[1];
    const withdrawRaw = parts[2];
    const depositRaw = parts[3];
    const balanceRaw = parts[4];
    const chq = parts[5];

    const withdrawal = withdrawRaw ? parseFloat(withdrawRaw) : null;
    const deposit = depositRaw ? parseFloat(depositRaw) : null;
    const balance = parseFloat(balanceRaw);
    const category = getCategoryFromDescription(desc);

    return {
      id: `${idx}-${date}-${balance}`,
      date,
      description: desc,
      withdrawal,
      deposit,
      balance,
      category,
      chqNo: chq || undefined,
      source: "preloaded"
    };
  });
}

